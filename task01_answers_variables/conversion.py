# Declare variables and assign values:
num1 = 99.23 # A float variable with the value 99.23
num2 = 23 # An integer variable with the value 23
num3 = 150 # An integer variable with the value 150
string1 = "100" # A string variable with the value "100"

# Convert variables to other types
num1_int = int(num1) # Convert the float variable num1 to an integer
num2_float = float(num2) # Convert the integer variable num2 to a float
num3_string = str(num3) # Convert the integer variable num3 to a string
string1_int = int(string1) # Convert the string variable string1 to an integer

# Print variable values
print(num1_int) # Print the value of the integer variable num1_int
print(num2_float) # Print the value of the float variable num2_float
print(num3_string) # Print the value of the string variable num3_string
print(string1_int) # Print  the value of the integer variable string1_int

