#The code uses a for loop to iterate through a range of integers from 1 to 5, inclusive. During each iteration, the variable i is assigned the next value in the range.
#Within the loop, the print function is used to output a string consisting of "*"

# Iterate through the range of values from 1 to 5 (inclusive)
for i in range(1, 6):

    # Print a string containing a number of asterisks (*) equal to the current value of i
    print('*' * i)
