# Declaring variables:
num1 = 99.23 # Declaring a float variable with value 99.23
num2 = 23 # Declaring an integer variable with value 23
num3 = 150 # Declaring an integer variable with value 150
string1 = "100" # Declaring a string variable with value 100

# Converting variables to other types:
num1_int = int(num1) # Converting float to int
num2_float = float(num2) # Converting int to float 
num3_string = str(num3) # Converting int to string
string1_int = int(string1) # Converting string to int

# Printing variables
print(num1_int) # Printing int variable num1_int
print(num2_float) # Printing float variable num2_float
print(num3_string) # Printing string variable num3_string
print(string1_int) # Printing int variable string1_int

